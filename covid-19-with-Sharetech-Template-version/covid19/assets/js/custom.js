$(document).ready(function() {


/*----------------------------------------------------------------------
------------------------------------------------------------------------
					Small Device Navigation
------------------------------------------------------------------------
----------------------------------------------------------------------*/ 
	$('#navigationMenu').click(function(){
		$('#smallNavigation').css("bottom","5px");
		$('#overlay').css("display","block");
		$(this).css("bottom","-1000px");
	});

	$('#smallNavigationCancel').click(function(){
		$('#smallNavigation').css("bottom","1000px");
		$('#overlay').css("display","none");
		$('#navigationMenu').css("bottom","0px");
	});
/*----------------------------------------------------------------------
------------------------------------------------------------------------
				End of Small Device Navigation
------------------------------------------------------------------------
----------------------------------------------------------------------*/ 
});